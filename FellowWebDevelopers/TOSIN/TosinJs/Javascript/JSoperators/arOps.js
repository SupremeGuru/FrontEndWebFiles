let fnum = 50
let snum = 30
// Plus operator
let sumRes = fnum + snum
console.log(sumRes);

// Minus operator
let diffRes = fnum - snum
console.log(diffRes);

// Time operator
let multiRes = fnum * snum
console.log(multiRes);

// divide operator
let dividRes = fnum / snum
console.log(dividRes);

// Exponential operator
let expoRes = fnum ** snum
console.log(expoRes);

// Modulus operator
let modRes = fnum % snum
console.log(modRes);