// Creating variables
var x = 2;
var y = 3.14;
var z = 0xff;

// Printing variable values
document.write(x + "<br>");
document.write(y + "<br>");
document.write(z);



// Exponential Numbers

// Creating variables
var x = 1.57e4; // same as 15700
var y = 4.25e+6; // same as 4250000
var z = 4.25-6; // same as 0.00000425

// Printing variable values
document.write(x + "<br>");
document.write(y + "<br>");
document.write(z);

// base 16 Hexadecimal

// Creating variables
var x = 0xff; // same as 255
var y = 0xb4; // same as 180
var z = 0x00; // same as 0

// Printing variable values
document.write(x + "<br>");
document.write(y + "<br>");
document.write(z);
