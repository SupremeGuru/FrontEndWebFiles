let val = '12'
console.log(+val);   // Unary Plus  convert values to number
console.log(++val);  // Pre Increment Operators   add 1 to a value 13
console.log(val++);  // Post Increment Operators  add 0 to a value 13

let num = 10;

console.log(-num);   // Unary Minus  convert values to number  Print: -10
console.log(--num);  // Pre Decrement Operators   subtracts 1 to a value 9
console.log(num--);  // Post Decrement Operators  subtracts 0 to a value 9



console.log(typeof(+val));   // This shows that adding + infront of val changes its type to a number 
console.log(typeof(-val));   // This shows that also adding - infront of val changes its type to a number 