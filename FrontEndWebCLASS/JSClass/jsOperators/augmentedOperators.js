// Augmented Operators are used to augment a value to a variable
let num = 50;

// Plus Augmented Operator
let augPlus = num += 10;    // It can also be like this num = num + 10
console.log(augPlus);

// Minus Augmented Operator // It can also be like this num = num - 10
let augMinus = num -= 20;
console.log(augMinus);

// Multiplication Augmented Operator    // It can also be like this num = num * 10
let augMulti = num *= 5;
console.log(augMulti);

// Division Augmented Operator  // It can also be like this num = num / 10
let augDiv = num /= 10;
console.log(augDiv);

// Exponent Augmented Operator  // It can also be like this num = num ** 10
let augExp = num **= 2;
console.log(augExp);

