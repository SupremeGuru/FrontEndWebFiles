// Arithemetic Operators
let fnum = 50
let snum = 30
// Using Plus Operator
let sumRes = fnum + snum
console.log(sumRes);

// Using Minus Operator
let diffRes = fnum - snum
console.log(diffRes);

// Using multiplication Operator
let multiRes = fnum * snum
console.log(multiRes);

// Using Division Operator
let diviRes = fnum / snum
console.log(diviRes);

// Exponent or Raise to power Operator
let expRes = fnum ** snum
console.log(expRes);

// Modulus Operator (Remainder giver)
let modRes = fnum % snum
console.log(modRes);
